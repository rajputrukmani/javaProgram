public class Pattern {
    public static void main(String[] args) {
        int i,j;
        for(i=1;i<=5;i++)
        {
      
            for(j=1;j<=i*2;j++)
            {
        
                System.out.print(j);
            }
          i++;
            System.out.println();

        }
    }
    
}
